// AI Education & Workforce Data

export type EducationCategory = 
  | 'school_students'
  | 'university_students'
  | 'general_employees'
  | 'industry_specific';

export type ProgramType = 
  | 'curriculum'
  | 'bootcamp'
  | 'certification'
  | 'degree'
  | 'workshop'
  | 'online_course'
  | 'apprenticeship'
  | 'fellowship';

export type SkillLevel = 'beginner' | 'intermediate' | 'advanced' | 'expert';

export type DeliveryMode = 'in_person' | 'online' | 'hybrid';

export interface Institution {
  id: string;
  name: string;
  type: 'k12_school' | 'college' | 'university' | 'corporate' | 'government' | 'nonprofit' | 'startup';
  province: string;
  city: string;
  logo?: string;
}

export interface Program {
  id: string;
  name: string;
  category: EducationCategory;
  type: ProgramType;
  institution: Institution;
  description: string;
  duration: string;
  skillLevel: SkillLevel;
  deliveryMode: DeliveryMode;
  cost: number; // CAD, 0 for free
  subsidized: boolean;
  governmentFunded: boolean;
  skills: string[];
  tools: string[];
  certification?: string;
  enrollmentCapacity: number;
  currentEnrollment: number;
  completionRate: number; // percentage
  employmentRate?: number; // percentage for job-oriented programs
  startDates: string[];
  language: 'en' | 'fr' | 'bilingual';
  ageRange?: string;
  prerequisites?: string[];
  outcomes: string[];
  partnerCompanies?: string[];
}

export interface WorkforceStats {
  category: EducationCategory;
  totalPrograms: number;
  totalEnrolled: number;
  avgCompletionRate: number;
  jobsCreated: number;
  skillGapReduction: number; // percentage
}

