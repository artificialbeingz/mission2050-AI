export const getHospitalTypeLabel = (type: HospitalType): string => {
  const labels: Record<HospitalType, string> = {
    general: 'General Hospital',
    specialized: 'Specialized Center',
    community: 'Community Hospital',
    teaching: 'Teaching Hospital',
    rehabilitation: 'Rehabilitation',
  };
  return labels[type];
};

export const getServiceLevelLabel = (level: ServiceLevel): string => {
  const labels: Record<ServiceLevel, string> = {
    primary: 'Primary Care',
    secondary: 'Secondary Care',
    tertiary: 'Tertiary Care',
    quaternary: 'Quaternary Care',
  };
  return labels[level];
};

